# Ruby program to check the size
# using the case statement

number = 47

case number #ambil nilai

when 29 #saat nilai 29 maka
  puts "Small"
when 42
  puts "Medium"
when 44
  puts "Large"
when 48
  puts "Extra Large"
else
  puts "Unknown"
end
#case digunakan untuk hasil output yang benar-benar sesuai!
